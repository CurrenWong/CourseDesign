package com.courseDesign.javabean;

public class major {
    public int classid;
    public int regioned;
    public String nmajor;
    public String nclass;
    public int kind;

    public int getClassid() {
        return classid;
    }

    public void setClassid(int classid) {
        this.classid = classid;
    }

    public int getRegioned() {
        return regioned;
    }

    public void setRegioned(int regioned) {
        this.regioned = regioned;
    }

    public String getNmajor() {
        return nmajor;
    }

    public void setNmajor(String nmajor) {
        this.nmajor = nmajor;
    }

    public String getNclass() {
        return nclass;
    }

    public void setNclass(String nclass) {
        this.nclass = nclass;
    }

    public int getKind() {
        return kind;
    }

    public void setKind(int kind) {
        this.kind = kind;
    }
}
